// Process all locale files
import { readdirSync } from "fs"
const messagesDir = process.argv[2] || "./src/i18n/messages"
const files = readdirSync(messagesDir).filter(f => f.endsWith(".json"))

for (const file of files) {
  const path = `${messagesDir}/${file}`
  const data = JSON.parse(readFileSync(path, "utf-8"))

  if (file === "fr.json") {
    data.categoryNames = FR_CATEGORY_NAMES
  } else {
    // For all other locales, use English names (they can be translated later)
    data.categoryNames = CATEGORY_NAMES
  }

  writeFileSync(path, JSON.stringify(data, null, 2) + "\n")
  console.log(`Updated ${file}`)
}

console.log("Done.")
